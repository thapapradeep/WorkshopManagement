    private void txt_fieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_fieldKeyTyped
        // TODO add your handling code here:
        int index=cmb_type.getSelectedIndex();
        String name=txt_field.getText();
        if(index==0){
            try {
                StaffType stt=new StaffType();
                stt.setStaffType(name);
                StaffController st=new StaffController();
                tbl_staff.setModel(st.buildStaffTable(stt));
            } catch (SQLException ex) {
                Logger.getLogger(searchStaff.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else if(index==1){
            try {
                Staff st=new Staff();
                st.setFname(name);
                StaffController staf=new StaffController();
                tbl_staff.setModel(TableManager.buildDataTable(staf.getStaff(st)));
            } catch (SQLException ex) {
                Logger.getLogger(searchStaff.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_txt_fieldKeyTyped

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(tbl_staff.getSelectedRow()!=-1){
            int memId=tbl_staff.getSelectedRow();
            int id=(Integer) tbl_staff.getValueAt(memId,0);
            Staff stt=new Staff();
            stt.setId(id);
            
            AttendenceController at=new AttendenceController();
            try {
                tbl_att.setModel(TableManager.buildDataTable(at.getAttendence(stt)));
            } catch (SQLException ex) {
                Logger.getLogger(searchStaff.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        // TODO add your handling code here:
        if(tbl_staff.getSelectedRow()!=-1){
            try {
                int memId=tbl_staff.getSelectedRow();
                int id=(Integer) tbl_staff.getValueAt(memId,0);
                Staff stt=new Staff();
                stt.setId(id);
                StaffController staf=new StaffController();
                int done=staf.deleteStaff(stt);
                if(done!=0){
                    JOptionPane.showMessageDialog(null," Selected Staff is deleted");
                    
                }
            } catch (SQLException ex) {
                Logger.getLogger(searchStaff.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_delActionPerformed

