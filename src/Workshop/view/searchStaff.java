    private void uppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uppActionPerformed
        // TODO add your handling code here:
        if(tbl_staff.getSelectedRow()!=-1){
            try {
                lbl_fname.setVisible(true);
                lbl_lname.setVisible(true);
                lbl_address.setVisible(true);
                lbl_contact.setVisible(true);
                lbl_staff.setVisible(true);;
                txt_fname.setVisible(true);
                txt_lname.setVisible(true);
                txt_add.setVisible(true);
                txt_contact.setVisible(true);
                cmb_satff.setVisible(true);
                btn_update.setVisible(true);
                
                int member=tbl_staff.getSelectedRow();
                int idd=(Integer) tbl_staff.getValueAt(member, 0);
                this.id=idd;
                String fname=String.valueOf(tbl_staff.getValueAt(member, 1));
                String lname=String.valueOf(tbl_staff.getValueAt(member, 2));
                String address=String.valueOf(tbl_staff.getValueAt(member, 3));
                int contact=Integer.parseInt(String.valueOf(tbl_staff.getValueAt(member, 4)));
                String type=String.valueOf(tbl_staff.getValueAt(member, 5));
                
                txt_fname.setText(fname);
                txt_lname.setText(lname);
                txt_add.setText(address);
                txt_contact.setText(String.valueOf(contact));
                
                StaffController st=new StaffController();
                ResultSet rs=st.getAllType();
                while(rs.next()){
                    String name=rs.getString("staff_type");
                    cmb_satff.addItem(name);
                }
                cmb_satff.setSelectedItem(type);
            } catch (SQLException ex) {
                Logger.getLogger(searchStaff.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_uppActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        try {
            // TODO add your handling code here:
            String fname=txt_fname.getText();
            String lname=txt_lname.getText();
            String address=txt_add.getText();
            int contact=Integer.parseInt(txt_contact.getText());
            String type=String.valueOf(cmb_satff.getSelectedItem());
            
            StaffType stt=new StaffType();
            stt.setStaffType(type);
            OtherController staf=new OtherController();
            int st_id=staf.getType(stt);
            stt.setId(st_id);
            
            Staff staff=new Staff();
            staff.setFname(fname);
            staff.setLname(lname);
            staff.setAddress(address);
            staff.setContact(contact);
            staff.setStaffTypeId(stt);
            staff.setId(id);
            
            StaffController sta=new StaffController();
            int done=sta.updateStaff(staff);
            if(done!=0){
                JOptionPane.showMessageDialog(null, "succesful");
                txt_add.setText("");
                txt_contact.setText("");
                txt_fname.setText("");
                txt_lname.setText("");
            }
            else{
               JOptionPane.showMessageDialog(null, "something went wrong"); 
            }
        } catch (SQLException ex) {
            Logger.getLogger(searchStaff.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_updateActionPerformed


