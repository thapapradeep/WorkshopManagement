    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            if(txt_name.getText().isEmpty()==true){
                JOptionPane.showMessageDialog(null, "Please enter name of inventory");
            }
            else if(txt_price.getText().isEmpty()==true){
                JOptionPane.showMessageDialog(null, "Please enter price");
            }
            else if(txt_unit.getText().isEmpty()==true){
                JOptionPane.showMessageDialog(null, "Please enter available price");
            }
            else{
            String name=txt_name.getText();
            float price= Float.valueOf(txt_price.getText());
            int unit=Integer.parseInt(txt_unit.getText());
            String type=String.valueOf(cmb_type.getSelectedItem());
            String model=String.valueOf(cmb_model.getSelectedItem());
            String supplier=String.valueOf(cmb_sup.getSelectedItem());
            Supplier supp=new Supplier();
            supp.setName(supplier);
            SupplierController sp=new SupplierController();
            int id=sp.getId(supp);
            CarController car=new CarController();
            CarModel mo=new CarModel();
            mo.setModel(model);
            int idd=car.getModelId(mo);
            mo.setId(idd);
            supp.setId(id);
            
            
            CarPart cp=new CarPart();
            cp.setName(name);
            cp.setPrice(price);
            cp.setAvailableUnit(unit);
            cp.setType(type);
            cp.setModelId(mo);
            cp.setSupplierId(supp);
            
            int done=car.insertCar(cp);
            if(done!=0){
                JOptionPane.showMessageDialog(null," new Inventroy added");
            }
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(addCar.class.getName()).log(Level.SEVERE, null, ex);
        }
        
                
    }//GEN-LAST:event_jButton1ActionPerformed


