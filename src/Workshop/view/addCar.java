    public void loadCombo() throws SQLException{
          CarController cs=new CarController();
        ResultSet rs=cs.getModel();
        while(rs.next()){
            cmb_model.addItem(rs.getString("model"));
        }
        SupplierController sp=new SupplierController();
        ResultSet rss=sp.getSupplier();
        while(rss.next()){
            String name=rss.getString(1);
            cmb_sup.addItem(name);
        }

    }

